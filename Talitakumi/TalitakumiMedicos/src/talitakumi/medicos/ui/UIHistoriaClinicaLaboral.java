    private void consignarIMC() {
        float i = 0;
        int p,t;
        String xx;
        try {
        p = (Integer)this.peso1.getValue();
        t = (Integer)this.talla1.getValue();
        if(t!=0)
         i = p / (2 * t / 100);

        this.imc1.setText(i+"");
        if(i < 18.5)
            xx="DELGADO";
        else
            if(i >=18.5 && i < 25)
                xx="NORMAL";
            else
                if(i >=25 && i < 30)
                    xx = "SOBREPESO";
                else
                    if(i >=30 && i < 35)
                        xx = "OBESO";
                    else
                        xx = "OBESIDAD MORBIDA";
        } catch(Exception ex) {

            xx = "imc";
        }
        labelimc1.setText(xx);
    }
