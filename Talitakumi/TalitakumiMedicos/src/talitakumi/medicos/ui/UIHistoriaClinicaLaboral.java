    private boolean calcularPresionesSegundaToma() {

        boolean st;
        //int pts = Integer.parseInt(this.jtbPTPSistolica.getText());
        int pts = (Integer)this.jspinnerSTSistolica.getValue();
        int ptd = (Integer)this.jspinnerSTDiastolica.getValue();

        if (pts >= 150 || ptd >= 90)
            st = true;
        else
            st = false;

        return(st);
    }


