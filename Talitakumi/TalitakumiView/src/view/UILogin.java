    public void cargarListaLugares(Vector<String> lugares) {

        int l = lugares.size();

        DefaultComboBoxModel dm = new DefaultComboBoxModel();

        for(int i=0;i!=l;i++) {
            dm.addElement(lugares.get(i));
        }
        
        jComboBox1.setModel(dm);
    }
