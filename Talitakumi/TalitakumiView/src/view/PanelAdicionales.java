    public ArrayList<Integer> getItemsElegidos() {

        ArrayList<Integer> resp = new ArrayList<Integer>();
        
        for(int i=0;i!=kcbl.getModel().getSize();i++) {

            JCheckBox jcb = (JCheckBox) kcbl.getModel().getElementAt(i);
            if(jcb.isSelected()) {
                resp.add(i);
            }
        }
        return(resp);
    }

