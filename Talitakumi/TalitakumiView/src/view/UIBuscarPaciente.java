    public void setDatosTabla(DefaultTableModel dtm) {

        tabla.setModel(dtm);
        tabla.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);

        TableColumn col = tabla.getColumnModel().getColumn(0);
        col.setPreferredWidth(100);
        col = tabla.getColumnModel().getColumn(1);
        col.setPreferredWidth(200);
        col = tabla.getColumnModel().getColumn(2);
        col.setPreferredWidth(200);
    }

    public void setPacienteListener(MouseListener pacienteListener) {
        tabla.addMouseListener(pacienteListener);
    }


