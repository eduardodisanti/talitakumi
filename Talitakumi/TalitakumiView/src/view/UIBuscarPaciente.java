    public void apuntarFilaPacientes(int i) {
        tabla.setRowSelectionInterval(i, i);
        Rectangle rect = tabla.getCellRect(i, 0, true);
        tabla.scrollRectToVisible(rect);

    }

    public int getItemElegido() {

        int idx = tabla.getSelectedRow();
        
        Integer cedulaElegida = (Integer) tabla.getValueAt(idx, 0);


        return(cedulaElegida);
    }

