    public int getClienteElegido() {

        int resp = 0;

        int x = clientes.getSelectedColumn();
        int y = clientes.getSelectedRow();

        resp = (Integer) clientes.getValueAt(y, 0);

        return(resp);
    }

    public HashMap<String, Float> getPrecios() {

        HashMap<String, Float> hm = new HashMap<String, Float>();

        for(int x=0;x!=precios.getRowCount();x++) {

            String clave;
            int id = (Integer)precios.getValueAt(x, 0);
            String val = (String) precios.getValueAt(x, 3);
            float precio = Float.parseFloat(val);

            clave = id + "-" + precios.getValueAt(x, 1);
            hm.put(clave, precio);
        }
        return(hm);
    }

